postare_index = {"i": 0}


def get_next_postare():
    postare = POSTARI[postare_index["i"] % len(POSTARI)]
    postare_index["i"] += 1
    return postare


async def get_unsplash_image(query: str) -> str | None:
    async with httpx.AsyncClient(timeout=15) as client:
        response = await client.get(
            "https://api.unsplash.com/photos/random",
            params={
                "query": query,
                "orientation": "landscape",
                "content_filter": "high",
            },
            headers={"Authorization": f"Client-ID {UNSPLASH_ACCESS_KEY}"},
        )
        if response.status_code == 200:
            data = response.json()
            return data["urls"]["regular"]
    return None


async def send_to_telegram(text: str, image_url: str | None):
    async with httpx.AsyncClient(timeout=30) as client:
        if image_url:
            await client.post(
                f"https://api.telegram.org/bot{TELEGRAM_TOKEN}/sendPhoto",
                json={
                    "chat_id": TELEGRAM_CHANNEL,
                    "photo": image_url,
                },
            )
        resp = await client.post(
            f"https://api.telegram.org/bot{TELEGRAM_TOKEN}/sendMessage",
            json={
                "chat_id": TELEGRAM_CHANNEL,
                "text": text,
            },
        )
        print(f"Telegram: {resp.status_code}")


async def posteaza():
    query, text = get_next_postare()
    print(f"[{datetime.now()}] Postare: {query[:30]}")
    try:
        image_url = await get_unsplash_image(query)
        await send_to_telegram(text, image_url)
        print(f"[{datetime.now()}] Postat cu succes!")
    except Exception as e:
        print(f"[{datetime.now()}] Eroare: {e}")


async def main():
    tz = pytz.timezone(TIMEZONE)
    scheduler = AsyncIOScheduler(timezone=tz)
    scheduler.add_job(posteaza, "cron", hour=9, minute=0)
    scheduler.add_job(posteaza, "cron", hour=19, minute=0)
    scheduler.start()
    print(f"Bot pornit! Postare la 09:00 si 19:00 ({TIMEZONE})")
    await posteaza()
    try:
        while True:
            await asyncio.sleep(3600)
    except (KeyboardInterrupt, SystemExit):
        scheduler.shutdown()


if __name__ == "__main__":
    asyncio.run(main())
